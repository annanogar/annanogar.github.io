/*
 * Generate Chunked Stylesheet Entrypoints
 *
 * Scans configured Nunjucks templates for component imports and creates Sass
 * entrypoints that contain only the component styles used by each chunk.
 */

import { mkdir, readFile, writeFile } from 'node:fs/promises'
import { dirname, relative as relativePath, resolve as resolvePath } from 'node:path'
import { glob as tGlob } from 'tinyglobby'

const TEMPLATE_REFERENCE_REGEX = /{%\s*(?:extends|import|include)\s+['"]([^'"]+\.html)['"]/g
const COMPONENT_TEMPLATE_REGEX = /^components\/(atoms|molecules|organisms|structures)\/([^/]+)\/\2\.html$/

const toPosixPath = path => path.replaceAll('\\', '/')

const normalizePath = path => toPosixPath(path).replace(/^\.?\//, '')

const resolveTemplateReference = reference => normalizePath(reference.startsWith('website/') ? reference : `website/${reference}`)

const toComponentStylesheet = templatePath => {
  const relativeTemplatePath = normalizePath(templatePath).replace(/^website\//, '')

  if (!COMPONENT_TEMPLATE_REGEX.test(relativeTemplatePath)) {
    return ''
  }

  return `website/${relativeTemplatePath.replace(/\.html$/, '.scss')}`
}

const getReferences = contents => [...contents.matchAll(TEMPLATE_REFERENCE_REGEX)].map(match => resolveTemplateReference(match[1]))

const getTemplateComponentStylesheets = async (templatePath, seenTemplates = new Set(), seenStylesheets = new Set()) => {
  templatePath = normalizePath(templatePath)

  if (seenTemplates.has(templatePath)) {
    return []
  }

  seenTemplates.add(templatePath)

  let contents = ''

  try {
    contents = await readFile(resolvePath(process.cwd(), templatePath), 'utf8')
  } catch {
    return []
  }

  const stylesheets = []

  for (const reference of getReferences(contents)) {
    const stylesheet = toComponentStylesheet(reference)

    if (stylesheet && !seenStylesheets.has(stylesheet)) {
      seenStylesheets.add(stylesheet)
      stylesheets.push(stylesheet)
    }

    stylesheets.push(...(await getTemplateComponentStylesheets(reference, seenTemplates, seenStylesheets)))
  }

  return stylesheets
}

const getComponentOrderIndex = (path, componentOrder = []) => {
  const [, category = ''] = normalizePath(path).match(/^website\/components\/([^/]+)\//) || []
  const index = componentOrder.indexOf(category)

  return index === -1 ? componentOrder.length : index
}

const sortComponentStylesheets = (stylesheets = [], componentOrder = []) =>
  [...stylesheets].sort((a, b) => {
    const orderDifference = getComponentOrderIndex(a, componentOrder) - getComponentOrderIndex(b, componentOrder)

    return orderDifference || a.localeCompare(b)
  })

const makeUseRule = (fromPath, toPath) => {
  const relative = normalizePath(relativePath(dirname(fromPath), toPath)).replace(/\.scss$/, '')

  return `@use '${relative.startsWith('.') ? relative : `./${relative}`}';`
}

const writeEntrypoint = async (path, imports = [], label = '') => {
  const contents = [`// Generated by sonic/actions/generate-chunked-stylesheets.js`, label ? `// ${label}` : '', '', ...imports.map(importPath => makeUseRule(path, importPath)), ''].filter(line => line !== '').join('\n')

  await mkdir(dirname(path), { recursive: true })
  await writeFile(resolvePath(process.cwd(), path), contents, 'utf8')
}

export default async function generateChunkedStylesheets(options = {}) {
  if (!options.sourcePath || !options.base || !options.chunks?.length) {
    return
  }

  const timestamp = new Date()
  const sourcePath = normalizePath(options.sourcePath)
  const baseName = options.base.name || 'base'
  const baseComponents = new Set((options.base.components || []).map(component => normalizePath(`website/${component.replace(/^website\//, '')}`)))
  const baseEntrypoint = `${sourcePath}/cunk.${baseName}.scss`
  const templateChunkMap = {}
  const generatedEntrypoints = [baseEntrypoint]

  await writeEntrypoint(baseEntrypoint, ['website/assets/stylesheets/base.scss', ...baseComponents], 'Shared base styles plus document/container structures.')

  for (const chunk of options.chunks) {
    const templatePaths = await tGlob(chunk.templateGlobs || [], { cwd: process.cwd(), absolute: false, onlyFiles: true })
    const chunkEntrypoint = `${sourcePath}/cunk.${chunk.name}.scss`
    const componentStylesheets = new Set()

    for (const templatePath of templatePaths) {
      templateChunkMap[normalizePath(templatePath)] = [`stylesheets/cunk.${baseName}.css`, `stylesheets/cunk.${chunk.name}.css`]

      for (const stylesheet of await getTemplateComponentStylesheets(templatePath)) {
        if (!baseComponents.has(stylesheet)) {
          componentStylesheets.add(stylesheet)
        }
      }
    }

    await writeEntrypoint(chunkEntrypoint, sortComponentStylesheets([...componentStylesheets], options.componentOrder), `Styles for the "${chunk.name}" page chunk.`)
    generatedEntrypoints.push(chunkEntrypoint)
  }

  global.chunkedStylesheetMap = templateChunkMap
  global.chunkedStylesheetEntrypoints = generatedEntrypoints
  global.useChunkedStylesheets = true

  if (global.logLevel !== 'quiet') {
    process.stdout.write(`    ${global.colors.count}${generatedEntrypoints.length}${global.colors.reset} chunked stylesheet entrypoints generated ${global.colors.timing}(${(new Date() - timestamp).toString()}ms)${global.colors.reset}\n`)
  }
}
